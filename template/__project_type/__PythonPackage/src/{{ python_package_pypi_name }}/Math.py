"""Basic math functions. This file illustrates how to create a python package that contains functions that can be invoked by other python code."""


# ----------------------------------------------------------------------
def Add(x, y):
    return x + y


# ----------------------------------------------------------------------
def Sub(x, y):
    return x - y


# ----------------------------------------------------------------------
def Mult(x, y):
    return x * y


# ----------------------------------------------------------------------
def Div(x, y):
    return x / y
